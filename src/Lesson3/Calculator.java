package Lesson3;

public class Calculator {
    public int sum(int a, int b) {
        return a + b;
    }

    public int diff (int a, int b) {
        return a - b;
    }

    public int multiply (int a, int b) {
        return a * b;
    }

    public int divide (int a, int b) {
        return a / b;
    }
}
